    public void showTable() throws IOException {
        DefaultTableModel model = (DefaultTableModel) UserTable.getModel();
        model.setRowCount(0);
        String[] columnNames = {"User ID", "Full Name","Password", "Contact", "Username", "Role"}; 
        model.setColumnIdentifiers(columnNames);
        
        ArrayList<String[]> pt = User.view();
        for (String[] pl: pt) {
            model.addRow(pl);
        }
    }
    public void loadRole(){
        Role[] roles = Role.values();
        DefaultComboBoxModel roleModel = (DefaultComboBoxModel) cmbRole.getModel();
        cmbRole.removeAllItems();
        for (Role role:roles){
            roleModel.addElement(role.toString());
        }
        roleModel.addElement("");
        cmbRole.setSelectedIndex(-1);
    }

