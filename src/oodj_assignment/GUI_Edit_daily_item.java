    private void SaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SaveActionPerformed
        String errorMessage = null;
        while (true){
            if (!(InputValidation.isValidQuantity(txtAmount.getText()))){
                errorMessage = "Invalid Quantity";
                break;
            } else{
                DailyItemSales newDIS = new DailyItemSales(dis.getDate(),Integer.parseInt(txtAmount.getText()),dis.getItem());
                try{
                    if (admin != null){
                        admin.editDIS(dis, newDIS);
                        break;
                    } else if (sm != null){
                        sm.editDIS(dis,newDIS);
                        break;
                    }

                } catch(IOException e){
                    break;
                }
                    
            }
        }
        if (errorMessage != null){
            JOptionPane.showMessageDialog(null,errorMessage);
        } else{
            if (admin != null){
                GUI_Daily_item_Entry disEntry = new GUI_Daily_item_Entry(admin);
                disEntry.show();
                this.dispose();
            } else if(sm != null){
                GUI_Daily_item_Entry disEntry = new GUI_Daily_item_Entry(sm);
                disEntry.show();
                this.dispose();
            }
        }
    }//GEN-LAST:event_SaveActionPerformed
    
